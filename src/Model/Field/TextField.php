namespace Eddmash\PowerOrm\Model\Field;

use Doctrine\DBAL\Types\Type;
use Eddmash\PowerOrm\Backends\ConnectionInterface;
use Eddmash\PowerOrm\Form\Widgets\TextArea;
use Eddmash\PowerOrm\Helpers\ArrayHelper;

class TextField extends Field
{
    /**
     * {@inheritdoc}
     */
    public function dbType(ConnectionInterface $connection)
    {
        return Type::TEXT;
    }

    /**
     * {@inheritdoc}
     */
    public function formField($kwargs = [])
    {
        $kwargs['max_length'] = $this->maxLength;
        $kwargs['widget'] = ArrayHelper::getValue($kwargs, 'widget', TextArea::class);

        return parent::formField($kwargs); // TODO: Change the autogenerated stub
    }
}
